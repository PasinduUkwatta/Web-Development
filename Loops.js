//for loop can be used when the no of iterations an be known

for(i=0;i<=10;i++){
    console.log("number is :"+i);

};


//while loop can be used for the no of iterations cannotbe known
//we should declare the variable at first
console.log("while loop started");
i=0;
while(i<=10){
    console.log("number is :"+i);
    i++;
};


//do while is used when first time itetrations should do
//should declare the no iterations known
console.log("do while loop started");
i=0;
do{
    console.log("number is :"+i);
    i++;
}while(i<=10);
